// 7 - 6 5 - 5 4 4 3 - 4 3 
int d[42][2];
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);

    int t;
    cin >> t;
    d[0][0] = 1;
    d[0][1] = 0;
    d[1][0] = 0;
    d[1][1] = 1;
    d[2][0] = 1;
    d[2][1] = 1;
    d[3][0] = 1;
    d[3][1] = 2;
    d[4][0] = 2;
    d[4][1] = 3;
    for (int i = 5;i<=42;i++){
        d[i][0] = d[i-1][0] + d[i-2][0];
        d[i][1] = d[i-1][1] + d[i-2][1];
    }
    while (t--){
        int n;
        cin >> n;
        cout << d[n][0] << ' '<<d[n][1]<<"\n";

    }

    return 0;
}